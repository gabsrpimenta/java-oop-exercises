package exercicio3;

public class Condicionais {

    public static void main(String[] args) {

        int idade = 24;

        if (idade >= 18) {
            System.out.println("Maior de idade");
        } else {
            System.out.println("Menor de idade");
        }

        for (int i = 1; i <= 10; i++) {
            System.out.println("Repetição de " + i);
        }
    }
}